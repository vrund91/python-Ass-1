''' Write a Python program that will demonstrate the use of Arithmetic Operators'''
x=float(input("Enter a number:"))
y=float(input("Enter a number:"))

print("1.x + y")
print("Addition of two number: ", x + y)
print("\n")

print("2.x - y")
print("subtraction of two number: ", x - y)
print("\n")

print("3.x * y")
print("Multiplication of two number: ",x * y)
print("\n")

print("4.x / y")
print("Division of two number: ",x / y)
print("\n")

print("5.x % y")
print("Modular of two number: ",x % y)
print("\n")

print("6.x // y")
print("floor Division of two number: ",x // y)
print("\n")

print("7.x**y")
print("Exponent of two number: ",x**y)
print("\n")