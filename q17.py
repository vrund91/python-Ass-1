''' Write a Python program that will demonstrate the use of Comparison Operators.'''

x=float(input("Enter a number:"))
y=float(input("Enter a number:"))

print("1.x > y")
print(f"comparison {x} > {y} of two value: " ,x > y)
print("\n")

print("2.x < y")
print(f"comparison {x} < {y} of two value: " ,x < y)
print("\n")

print("3.x == y")
print(f"comparison {x} == {y} of two value: " ,x == y)
print("\n")

print("4.x != y")
print(f"comparison {x} != {y} of two value: " ,x != y)
print("\n")

print("5.x >= y")
print(f"comparison {x} >= {y} of two value: " ,x >= y)
print("\n")

print("6.x <= y")
print(f"comparison {x} <= {y} of two value: " ,x <= y)
print("\n")